#Operadores Aritmeticos
#adição
n1=5+2
print('A soma de 5 + 2 é {}'.format(n1))
#subtração
n2=10-7
print('A subtração de 10 - 7 é {}'.format(n2))
#multiplicação
n3=3*8
print('a multiplicação de  3 X 8 é {}'.format(n3))
#divisão
n4=15/2
print('A divisão de 15/2 é {}'.format(n4))
#potencia
n5=2**3
print('A potencia de 2 elevado a 3 é {}'.format(n5))
#divisão inteira
n6=15//2
print('a divisão inteira de 15/2 é {}'.format(n6))
#resto ou modulo
n7=10%2
n8=10%3
print('O resto ou modulo de 10 por 2 é {}'.format(n7))
print('o resto ou modulo de 10 por 3 é {}'.format(n8))
n9=2+14
print('A soma de 2+14 é \n {:.3f}'.format(n9))
